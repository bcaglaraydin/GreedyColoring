//function to return ajdacent vertices to the given vertex
vector <int> get_adj(int v, vector<vector<bool>> adj) {
	vector<int> v_adj;
	for (int j = 0; j < adj[v].size(); j++)
	{
		if (adj[v][j]) {
			v_adj.push_back(j);
		}
	}
	return v_adj;
}

